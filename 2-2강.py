# 숫자의 종류
print(type(52))  # 정수는 int
print(type(52.273))  # 실수는 float

# 사칙 연산자
print("5+7=", 5 + 7)
print("5-7=", 5 - 7)
print("5*7=", 5 * 7)
print("5/7=", 5 / 7)

# 정수 나누기 연산자 //
print("3//2=", 3 // 2)  # 소수점 이하의 자릿수 버리기
# 나머지 연산자 %
print("5%2=", 5 % 2)  # 나머지 구하기
# 제곱 연산자 **
print("2**1=", 2 ** 1)
print("2**4=", 2 ** 4)
# 먼저 연산하고 싶은 부분을 괄호로 감싸준다.
